#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    // trabalhando com numeros

    cout << pow(2, 5) << endl;
    cout << sqrt(4) << endl;
    cout << round(4.5) << endl; // arrendonda
    cout << ceil(4.1) << endl;  // arredonda para cima
    cout << floor(4.8) << endl; // arredonda para baixo
    cout << fmax(3, 8) << endl; // retorna o maior valor
    cout << fmin(3, 8) << endl; // retorna o menor valor

    //  pesquisar funções matemáticas

    return 0;
}